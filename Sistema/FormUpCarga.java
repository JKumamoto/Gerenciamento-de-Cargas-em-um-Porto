    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //btn alterar
 		try{
			if(jTextField1.getText().equals("")||(jTextField2.getText().equals("")&&jTextField3.getText().equals("")&&
				jTextField4.getText().equals("")&&jTextField5.getText().equals("")))
				throw new RuntimeException("Preencha o ID e o campo que deve ser mudado");
      
			int id=Integer.parseInt(jTextField1.getText());
			if(id<0)	throw new RuntimeException("ID invalido");	
			String previsto="";
			if(!jTextField2.getText().equals("")){
				SimpleDateFormat sdf=new SimpleDateFormat("dd/MM/yyyy");
				sdf.setLenient(false);
				Calendar cal=Calendar.getInstance();
				Date date1=sdf.parse(jTextField2.getText());
				cal.setTime(date1);
				int mes=cal.get(Calendar.MONTH)+1;
				previsto=cal.get(Calendar.YEAR)+"-"+mes+"-"+cal.get(Calendar.DATE);
			}
			Carga c=new Carga("", false, previsto, jTextField3.getText(), jTextField4.getText(), jTextField5.getText());
			c.setID(id);
		
			Requisicao req=new Requisicao();
			req.setTipo(Requisicao.AlteraCarga);
			req.setCarga(c);
			Server_Interface server=(Server_Interface)Naming.lookup("rmi://localhost/Servidor");
			Resposta rep=server.AlteraCarga(req);
			int tipo=rep.getTipo();

			if(tipo==Resposta.AlteraCarga)
				JOptionPane.showMessageDialog(this, "Carga alterada com sucesso.");
			else if(tipo==Resposta.ErroID)
				throw new RuntimeException("ID invalido");
			else if(tipo==Resposta.ErroAlteraCarga)
				throw new RuntimeException("Erro\nAlteracao nao realizada");
			else
				throw new RuntimeException("Alteracao nao realizada\nErro Desconhecido");

			this.dispose();
			new FormUpCarga().setVisible(true);
		}catch(NumberFormatException e){
             JOptionPane.showMessageDialog(this, "Digite o CPF apenas com numeros");
    	}catch(ParseException e){
			JOptionPane.showMessageDialog(this, "Digite a data no formato dd/mm/aaaa");
	    }catch(RuntimeException e){
			JOptionPane.showMessageDialog(this, ""+e.getMessage());
		}catch(Exception e){
			JOptionPane.showMessageDialog(this, "Falha na ConexÃ£o com o Servidor");
		}	
       
        
    }//GEN-LAST:event_jButton1ActionPerformed

